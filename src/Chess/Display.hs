module Chess.Display where

import Chess.Internal
import Data.Maybe (maybe)

data DisplayMode = Game | Debug deriving (Eq, Show)

pieceAt :: Coord -> Board -> Figure
pieceAt coord board = maybe (Empty, W) extract $ lookAt board coord
    where extract (Pos p c _) = (p, c)

